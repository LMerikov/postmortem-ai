from datetime import datetime


SEVERITY_LABELS = {"P0": "🔴 P0 - Critical", "P1": "🟠 P1 - High", "P2": "🟡 P2 - Medium", "P3": "🔵 P3 - Low", "P4": "⚪ P4 - Informational"}
PRIORITY_LABELS = {"HIGH": "🔴 HIGH", "MEDIUM": "🟡 MEDIUM", "LOW": "🟢 LOW"}
TIMELINE_ICONS = {"alert": "🚨", "action": "⚡", "escalation": "📣", "resolution": "✅", "detection": "🔍"}


def generate_markdown(postmortem: dict) -> str:
    severity = postmortem.get("severity", "P3")
    severity_label = SEVERITY_LABELS.get(severity, severity)

    lines = [
        f"# 🔥 Postmortem: {postmortem.get('title', 'Untitled Incident')}",
        f"",
        f"**Severity:** {severity_label}  ",
        f"**Generated:** {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}  ",
        f"**Source:** Postmortem.ai",
        f"",
        f"---",
        f"",
        f"## 📋 Executive Summary",
        f"",
        postmortem.get("summary", ""),
        f"",
        f"---",
        f"",
        f"## 📅 Timeline",
        f"",
        f"| Time | Event | Type |",
        f"|------|-------|------|",
    ]

    for entry in postmortem.get("timeline", []):
        icon = TIMELINE_ICONS.get(entry.get("type", ""), "•")
        lines.append(f"| `{entry.get('time', '')}` | {entry.get('event', '')} | {icon} {entry.get('type', '').title()} |")

    lines += [
        f"",
        f"---",
        f"",
        f"## 🔍 Root Cause Analysis",
        f"",
        postmortem.get("root_cause", ""),
        f"",
        f"---",
        f"",
        f"## 💥 Impact",
        f"",
    ]

    impact = postmortem.get("impact", {})
    lines += [
        f"- **Users Affected:** {impact.get('users_affected', 'Unknown')}",
        f"- **Duration:** {impact.get('duration', 'Unknown')}",
        f"- **Services Affected:** {', '.join(impact.get('services_affected', []))}",
        f"- **Revenue Impact:** {impact.get('revenue_impact', 'Unknown')}",
        f"",
        f"---",
        f"",
        f"## ⚡ Actions Taken During Incident",
        f"",
    ]
    for action in postmortem.get("actions_taken", []):
        lines.append(f"- {action}")

    lines += [
        f"",
        f"---",
        f"",
        f"## ✅ Action Items",
        f"",
    ]
    for item in postmortem.get("action_items", []):
        priority_label = PRIORITY_LABELS.get(item.get("priority", "MEDIUM"), item.get("priority", ""))
        lines.append(f"- **[{priority_label}]** {item.get('description', '')} *(Owner: {item.get('owner', 'TBD')})*")

    lines += [
        f"",
        f"---",
        f"",
        f"## 📖 Lessons Learned",
        f"",
    ]
    for i, lesson in enumerate(postmortem.get("lessons_learned", []), 1):
        lines.append(f"{i}. {lesson}")

    lines += [
        f"",
        f"---",
        f"",
        f"## 📊 Monitoring Recommendations",
        f"",
    ]
    for rec in postmortem.get("monitoring_recommendations", []):
        lines.append(f"- {rec}")

    lines += [
        f"",
        f"---",
        f"",
        f"*Generated by [Postmortem.ai](https://postmortem-ai.cubepath.app) — From chaotic logs to professional postmortems in seconds.*",
    ]

    return "\n".join(lines)
